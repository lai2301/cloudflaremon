/**
 * Returns an inline SVG <use> reference for the given icon name.
 * NOTE: name is NOT sanitised — use only with hardcoded/internal names.
 * @param {string} name - Icon name without the "icon-" prefix
 * @param {string} extraClass - Additional CSS classes to append
 * @returns {string} SVG markup string
 */
export function icon(name, extraClass = '') {
  const cls = extraClass ? `icon ${extraClass}` : 'icon';
  return `<svg class="${cls}" aria-hidden="true"><use href="#icon-${name}"/></svg>`;
}
